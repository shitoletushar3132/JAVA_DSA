package Topics;

public class UnaryOperators {

    public static void main(String[] args) {
        // unary operators are used with one operand. for eg: ++

        /*
        * if i use ++ operator as a prefix like ++val, the value of val is incremented
        * by 1 , the it returns the value
        * */

        /*
         * if i use ++ operator as a postfix like val++, the original value of val
         * is returned first and val is incremented by 1.
         */

        int num =5;
        System.out.println(++num);
    }
}
